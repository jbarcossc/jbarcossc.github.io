function setPattern(rows, cols, pattern){
    let newGrid = new Array;
    for(let i = 0; i < rows; i++){
        newGrid.push(new Array);
        for(let j = 0; j < cols; j++){
            newGrid[newGrid.length - 1].push(0);
        }
    }
    let halfX = Math.floor(rows/2);
    let halfY = Math.floor(cols/2);
    for(let i = 0; i < 18; i++){
        for(let j = 0; j < 18; j++){
            newGrid[i+halfX-9][j+halfY-9] = pattern[i][j];
        }
    }
    return newGrid;
}
